## Задача 24: 
# В фермерском хозяйстве в Карелии выращивают чернику. 
# Она растёт на круглой грядке, причём кусты высажены только по окружности. 
# Таким образом, у каждого куста есть ровно два соседних. Всего на грядке растёт N кустов.
# Эти кусты обладают разной урожайностью, поэтому ко времени сбора на них выросло различное число ягод
#  — на i-ом кусте выросло ai ягод.
# В этом фермерском хозяйстве внедрена система автоматического сбора черники. 
# Эта система состоит из управляющего модуля и нескольких собирающих модулей. 
# Собирающий модуль за один заход, находясь непосредственно перед некоторым кустом, собирает ягоды с этого куста и с двух соседних с ним.
# Напишите программу для нахождения максимального числа ягод, которое может собрать за один заход собирающий модуль, находясь перед некоторым кустом 
# ????заданной во входном файле грядки.????


# Ввод количества кустов
N = int(input("Введите количество кустов: "))

# Ввод количества ягод на каждом кусте
berries = []
for i in range(N):
    berries.append(int(input(f"Введите количество ягод на кусте {i + 1}: ")))

# Создание списка для сумм ягод слева и справа от каждого куста
left_sum = [0] * N
right_sum = [0] * N

# Вычисление сумм ягод слева от каждого куста
current_sum = 0
for i in range(N):
    left_sum[i] = current_sum
    current_sum += berries[i]

# Вычисление сумм ягод справа от каждого куста
current_sum = 0
for i in range(N - 1, -1, -1):
    right_sum[i] = current_sum
    current_sum += berries[i]

# Нахождение максимальной суммы ягод
max_berries = max(berries[i] + left_sum[i] + right_sum[i] - berries[i] for i in range(N))

# Вывод результата
print(f"Максимальное количество ягод, которое можно собрать одним собирающим модулем: {max_berries}")
